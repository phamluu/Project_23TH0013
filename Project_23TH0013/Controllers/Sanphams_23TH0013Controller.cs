using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using Project_23TH0013.Models;

namespace Project_23TH0013.Controllers
{
    public class Sanphams_23TH0013Controller : Controller
    {
        private Project_23TH0013Entities db = new Project_23TH0013Entities();

        // GET: Sanphams_23TH0013
        public ActionResult Index()
        {
            return View(db.Sanphams.ToList());
        }

        // GET: Sanphams_23TH0013/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Sanpham sanpham = db.Sanphams.Find(id);
            if (sanpham == null)
