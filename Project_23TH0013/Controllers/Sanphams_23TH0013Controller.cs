        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "MaCake,TenCake,Giatien,Soluong,Mota,Size,HinhAnh,Ma_loai")] Sanpham sanpham)
        {
            if (ModelState.IsValid)
            {
