        public ActionResult Edit([Bind(Include = "MaCake,TenCake,Giatien,Soluong,Mota,Size,HinhAnh,Ma_loai")] Sanpham sanpham)
        {
            if (ModelState.IsValid)
            {
                db.Entry(sanpham).State = EntityState.Modified;
